#Stacks !!

#-> PUSH    : insert elements in a stack!
#-> POP     : delete elements from stack
#-> TOP     : access the top element of the stack
#-> SIZE    : size of the stack
#-> isEmpty : boolean function to tell if stack is empty or not


# Stacks can only be worked on 1 after the other !!

# stacks are LIFO structures   : Last in First Out !!
# LinkList are FIFO structures : First in First Out !!